package com.green.day3;

//이중 for문, 중첩 for문
public class MultipleForLoop {
    public static void main(String[] args) {

        for(int z=0; z<100; z++) {
            for(int i=0; i<5; i++) {
                System.out.print("*");
            }
            System.out.println();
        }


    }
}
