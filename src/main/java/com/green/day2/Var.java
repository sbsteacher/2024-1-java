package com.green.day2;
public class Var {
    public static void main(String[] args) {
        int num; //변수 선언 declare, 값을 담기 위해
        num = 10; //대입 연산자
        System.out.println(num);

        num = 20; //리터럴 (literal)
        System.out.println(num);

        num = 20 + 30;
        System.out.println(num);

        num = 10 * 40;
        System.out.println(num);

        num = 10 / 2;
        System.out.println(num);

        num = 10 / 3;
        System.out.println(num);

        num = 10 - 3;
        System.out.println(num);

        num = 10 % 2;
        System.out.println(num);

        num = 9 % 2;
        System.out.println(num);
    }
}
